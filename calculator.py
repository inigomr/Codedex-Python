def add(a, b):
  sum = a + b
  return sum

def subtract(a, b):
  difference = a - b
  return difference

def multiply(a, b):
  product = a * b
  return product

def divide(a, b):
  quotient = a / b
  return quotient

def exp(a, b):
  result = a ** b
  return result

print(add(2, 4))
print(subtract(2, 4))
print(multiply(2, 4))
print(divide(2, 4))
print(exp(2, 4))